package yokohama.osm.json.model;

public class JSONRequest {

    /**
     * ID
     */
    private String id;

    /**
     * パスワード
     */
    private String password;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
